# All J24 stores sit in Bengaluru (CITY-139779) per the pilot dataset.
CITY_NAME = "Bengaluru"


def _load() -> list[dict]:
    rows: list[dict] = []
    reader = csv.reader(io.StringIO(_RAW), delimiter="\t")
    for store_id, name, org_id, facility_id in reader:
        rows.append({
            "store_id": store_id,
            "name": name,
            "org_id": org_id,
            "facility_id": facility_id,
            "city": CITY_NAME,
        })
    rows.sort(key=lambda r: r["name"].lower())
    return rows


STORE_DIRECTORY: list[dict] = _load()
_BY_ID: dict[str, dict] = {r["store_id"]: r for r in STORE_DIRECTORY}

# Default pilot store: J24 - Essentials BTM Layout.
DEFAULT_STORE_ID = "BZID-1304298141"


def get_store(store_id: str) -> dict | None:
    return _BY_ID.get(store_id)
